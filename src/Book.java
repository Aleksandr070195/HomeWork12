public class Book {
    String author;
    String work;
    int year;

    public Book(String author, String work, int year) {
        this.author = author;
        this.work = work;
        this.year = year;
    }

    public String getAuthor() {
        return this.author;
    }

    public String getWork() {
        return this.work;}

    public int getYear() {
        return this.year;}

public void setYear(int year) {
    this.year = year;
}}

